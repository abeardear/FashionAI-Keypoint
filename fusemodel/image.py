        hsv = BGR2HSV(bgr)
        # h,s,v = cv2.split(hsv)
        h,s,v = hsv[:,:,0], hsv[:,:,1], hsv[:,:,2]
        adjust = random.choice([0.5,1.5])
        h = h*adjust
